import mongoose, { Schema, Document } from 'mongoose';

export interface IPrediction extends Document {
  raceId: mongoose.Types.ObjectId;
  question: string;              // Generated by Gemini
  optionA: string;               // Yes-type option
  optionB: string;               // No-type option
  poolA: number;                 // Track total Game Coins wagered on Option A
  poolB: number;                 // Track total Game Coins wagered on Option B
  status: 'open' | 'locked' | 'settled'; // manages betting windows
  correctOption: 'A' | 'B' | null;
  isSettled: boolean;
  createdAt: Date;
}

const PredictionSchema = new Schema<IPrediction>({
  raceId: { type: Schema.Types.ObjectId, ref: 'Race', required: true },
  question: { type: String, required: true },
  optionA: String,
  optionB: String,
  poolA: { type: Number, default: 0 },
  poolB: { type: Number, default: 0 },
  status: { type: String, enum: ['open', 'locked', 'settled'], default: 'open' },
  correctOption: { type: String, enum: ['A', 'B', null], default: null },
  isSettled: { type: Boolean, default: false },
  createdAt: { type: Date, default: Date.now },
});

export default mongoose.model<IPrediction>('Prediction', PredictionSchema);